"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Jorge Medina')
print(1, 'Jorge "Medina"')

# Aspas duplas
print("Jorge Medina")
print(2, "Jorge 'Medina'")

# Escape
print("Jorge \"Medina\"")

# r
print(r"Jorge \"Medina\"")